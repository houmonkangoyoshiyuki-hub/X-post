def build_account_research_prompt(profile_url: str) -> str:
    """
    web_searchツールを使わせる前提のプロンプト。
    相手アカウントのプロフィール・投稿傾向を調べ、共感ポイントをJSONで返させる。
    """
    return f"""以下のX(旧Twitter)アカウントについて、web検索ツールを使って調査してください。

対象URL: {profile_url}

# 調査してほしいこと
- プロフィール文(自己紹介)の内容
- 投稿から読み取れる、その人の状況・興味関心・悩みごとの傾向
- 「精神科ナースであり8人の子を持つパパ」というキャラクターが
  自然に共感を示せそうな具体的なポイント(子育て、心の疲れ、仕事の悩み、
  家族関係、生きづらさなど)
- 全体的な発信のトーン(明るい/シリアス/皮肉っぽい/淡々としている 等)

# 注意事項(重要)
- X(旧Twitter)は外部からの閲覧に制限があるため、取得できる情報が
  限定的な場合があります。無理に情報を捏造せず、わかった範囲で答えること
- 投稿本文を長く引用しないこと。内容は必ず自分の言葉で要約すること
  (どうしても引用が必要な場合も15語程度・30文字以内の短いフレーズに留める)

# 出力形式(厳守)
必ず以下のJSON形式のみを出力してください。前置き・説明文・Markdownのコードブロック
記法(```)は一切つけないこと。

{{
  "profile_summary": "プロフィールや投稿から見える人物像の要約",
  "recurring_themes": ["繰り返し見られる話題1", "話題2", "話題3"],
  "empathy_points": ["共感を示せそうな具体的なポイント1", "ポイント2"],
  "tone_notes": "その人の発信のトーンについての短いメモ",
  "research_confidence": "high, medium, low のいずれか(情報の充実度の自己評価)"
}}
"""


def build_account_reply_prompt(
    account_context: dict,
    manual_posts: str,
    past_posts: list[str],
    num_variants: int = 3,
) -> str:
    """
    build_account_research_prompt() の調査結果(dict)と、
    任意で手動追加された相手の投稿(manual_posts)をもとに、
    その人への「はじめてのリプライ」を生成させるプロンプト。
    """
    if past_posts:
        joined = "\n---\n".join(p.strip() for p in past_posts if p.strip())
        past_posts_block = (
            "# 自分(子だくさんナース)の過去の投稿(トーン一貫性の参考)\n"
            f"{joined}\n"
        )
    else:
        past_posts_block = (
            "# 自分(子だくさんナース)の過去の投稿\n"
            "(今回は参考投稿の指定なし。基本キャラ設定のみで判断すること)\n"
        )

    manual_block = ""
    if manual_posts and manual_posts.strip():
        manual_block = (
            "# ユーザーが手動で追加した、相手アカウントの投稿(補足情報・精度向上用)\n"
            f"{manual_posts.strip()}\n"
        )

    themes = "、".join(account_context.get("recurring_themes", []) or []) or "特になし"
    empathy = "、".join(account_context.get("empathy_points", []) or []) or "特になし"

    context_block = f"""# 相手アカウントの調査結果
- プロフィール要約: {account_context.get('profile_summary', '(情報なし)')}
- よく見られる話題: {themes}
- 共感を示せそうなポイント: {empathy}
- 発信のトーン: {account_context.get('tone_notes', '(情報なし)')}
- 調査の情報充実度: {account_context.get('research_confidence', '不明')}
"""

    return f"""以下の調査結果をもとに、そのアカウントの持ち主に向けて、
あなた(子だくさんナース)から「はじめて送るリプライ」を{num_variants}パターン考えてください。

特定の1投稿への返信ではなく、その人の投稿傾向や状況全体を踏まえたうえで、
最も共感できそうなポイントに触れる形で書いてください。
調査の情報充実度が低い場合は、無理に具体的な話に踏み込みすぎず、
一般的だが的外れにならない範囲の言葉にとどめてください。

{context_block}
{manual_block}
{past_posts_block}
# 出力形式(重要・厳守)
必ず以下のJSON配列のみを出力してください。前置き、説明文、Markdownのコードブロック記法(```)は
一切つけないこと。JSON以外の文字を出力に含めないこと。

[
  {{"reply": "返信文1", "reason": "どの共感ポイントに触れたか・このトーンにした理由(30〜50文字程度)"}},
  {{"reply": "返信文2", "reason": "..."}},
  {{"reply": "返信文3", "reason": "..."}}
]
"""


